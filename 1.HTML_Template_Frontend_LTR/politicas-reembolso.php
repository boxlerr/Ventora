<p><strong>Cómo devolver los productos:</strong></p>
<p>Siguiendo lo dispuesto en la Ley Federal de Protección al Consumidor, los gastos de envío de devolución de productos por insatisfacción o por arrepentimiento el costo de devolución correrá a cargo del cliente, sin embargo en caso de devolución por productos defectuosos o con daños ocasionados durante el envío, el costo de devolución corre por nuestra cuenta.</p>
